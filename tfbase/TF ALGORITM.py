## BFS

def bfs(G, s):
  n = len(G)
  visited = [False]*n
  path = [None]*n
  queue = [s]
  visited[s] = True

  while queue:
    u = queue.pop(0)
    for v in G[u]:
      if not visited[v]:
        visited[v] = True
        path[v] = u
        queue.append(v)

    return path

